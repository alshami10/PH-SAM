def map_centroids_to_original(img_gray, c1, c2):
    h, w = img_gray.shape[:2]
    top = int(h * TOP_RATIO)
    bottom = int(h * (1 - BOTTOM_RATIO))
    left = int(w * LEFT_RATIO)
    right = int(w * (1 - RIGHT_RATIO))
    crop_h = bottom - top

    # c1: add offsets
    x1o = left + int(c1[0])
    y1o = top  + int(c1[1])

    # c2: unflip vertically within the crop, then add offsets
    y2_unflipped = (crop_h - 1) - int(c2[1])
    x2o = left + int(c2[0])
    y2o = top  + y2_unflipped

    # clamp
    x1o = np.clip(x1o, 0, w-1); y1o = np.clip(y1o, 0, h-1)
    x2o = np.clip(x2o, 0, w-1); y2o = np.clip(y2o, 0, h-1)
    return (int(x1o), int(y1o)), (int(x2o), int(y2o))

def dice_score_bin(pred_mask_bin, gt_mask_bin):
    a = pred_mask_bin.astype(bool)
    b = gt_mask_bin.astype(bool)
    inter = np.logical_and(a, b).sum()
    denom = a.sum() + b.sum()
    return 1.0 if denom == 0 else (2.0 * inter / (denom + 1e-8))

def find_gt_path(base_id, original_filename):
    exts = [".png", ".jpg", ".jpeg", ".bmp", ".tif", ".tiff"]

    # 1) exact same filename first
    candidate = os.path.join(GT_DIR, original_filename)
    if os.path.exists(candidate):
        return candidate

    # 2) base + _mask.<ext>
    for e in exts:
        candidate = os.path.join(GT_DIR, f"{base_id}_mask{e}")
        if os.path.exists(candidate):
            return candidate

    # 3) base + any ext
    for e in exts:
        candidate = os.path.join(GT_DIR, f"{base_id}{e}")
        if os.path.exists(candidate):
            return candidate

    return None

def segment_with_sam_on_original(predictor: SamPredictor, image_path, c1_orig, c2_orig, mask_index=0):
    """
    Runs SAM on the original image using two mapped centroids.
    Returns:
        mask_bin (H, W) np.uint8 (0/1)
        mask_img (H, W) np.uint8 (0/255)
    """
    bgr = cv2.imread(image_path, cv2.IMREAD_COLOR)
    if bgr is None:
        raise ValueError(f"Unreadable image: {image_path}")

    rgb = cv2.cvtColor(bgr, cv2.COLOR_BGR2RGB)
    predictor.set_image(rgb)

    pts = np.array([c1_orig, c2_orig], dtype=np.float32)
    labels = np.array([1, 1], dtype=np.int32)

    masks, scores, logits = predictor.predict(
        point_coords=pts,
        point_labels=labels,
        multimask_output=True
    )
    masks = np.asarray(masks)

    if masks.shape[0] == 0:
        raise RuntimeError("SAM returned zero masks.")

    if mask_index >= masks.shape[0]:
        raise RuntimeError(f"Requested mask_index={mask_index} but SAM returned only {masks.shape[0]} masks.")

    chosen = masks[mask_index]
    mask_bin = (chosen > 0).astype(np.uint8)
    mask_img = (mask_bin * 255).astype(np.uint8)
    return mask_bin, mask_img

