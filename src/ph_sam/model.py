def crop_center(image, top_crop_ratio=TOP_RATIO, bottom_crop_ratio=BOTTOM_RATIO,
                left_crop_ratio=LEFT_RATIO, right_crop_ratio=RIGHT_RATIO):
    h, w = image.shape
    top = int(h * top_crop_ratio)
    bottom = int(h * (1 - bottom_crop_ratio))
    left = int(w * left_crop_ratio)
    right = int(w * (1 - right_crop_ratio))
    return image[top:bottom, left:right]

def process_ph(img, flipped=False):
    """
    Your original logic with safe try/except:
      - crop center
      - optional vertical flip
      - blur + Canny
      - (optional) PH on edge points via ripser
      - largest contour -> centroid via moments
    Returns (cropped_gray, (cx,cy)) or (None, None) on error.
    """
    try:
        image = cv2.imread(img, cv2.IMREAD_GRAYSCALE)
        if image is None:
            raise ValueError("Unreadable image")
        image = crop_center(image)
        if flipped:
            image = cv2.flip(image, 0)

        blur = cv2.GaussianBlur(image, (5, 5), 0)
        edges = cv2.Canny(blur, 50, 150)

        # Optional PH step
        if USE_RIPSER:
            X = np.column_stack(np.where(edges > 0)).T
            if X.size == 0:
                raise ValueError("No edge points found for PH computation.")
            _ = ripser(X, maxdim=2)['dgms']  # not plotting, just keeping PH active

        contours, _ = cv2.findContours(edges, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
        if not contours:
            raise ValueError("No contours found.")
        largest_contour = max(contours, key=cv2.contourArea)

        M = cv2.moments(largest_contour)
        if M["m00"] == 0:
            raise ValueError("Invalid contour moment (m00 = 0).")

        centroid_x = int(M["m10"] / M["m00"])
        centroid_y = int(M["m01"] / M["m00"])
        return image, (centroid_x, centroid_y)

    except Exception as e:
        print(f"[SKIP] Error processing {os.path.basename(img)} (flipped={flipped}): {e}")
        return None, None

def run_ph_folder(folder_path):
    centroids = {}
    image_files = sorted(glob.glob(os.path.join(folder_path, "*.png")))  # adjust ext if needed
    if not image_files:
        print(f"[INFO] No .png files in {folder_path}")

    done, skipped = 0, 0
    for img_path in image_files:
        try:
            # original + flipped
            image, c1 = process_ph(img_path, flipped=False)
            flipped_image, c2 = process_ph(img_path, flipped=True)

            if image is None or c1 is None or flipped_image is None or c2 is None:
                skipped += 1
                continue

            filename = os.path.basename(img_path)
            centroids[filename] = [list(c1), list(c2)]

            if SAVE_PREVIEWS:
                vis = cv2.cvtColor(image, cv2.COLOR_GRAY2BGR)
                cv2.circle(vis, tuple(c1), 5, (0, 255, 0), -1)  # green = original
                cv2.circle(vis, tuple(c2), 5, (255, 0, 0), -1)  # blue  = flipped-in-crop coords
                cv2.imwrite(os.path.join(PREVIEW_DIR, f"PHoutput_{filename}"), vis)

            done += 1
        except Exception as e:
            print(f"[SKIP] Unexpected error {os.path.basename(img_path)}: {e}")
            skipped += 1

    print(f"[PH] done={done}, skipped={skipped}, total={len(image_files)}")
    return centroids, done, skipped, len(image_files)

