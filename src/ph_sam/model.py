for model_name, folder in MODELS.items():
    folder_path = os.path.join(OUT_ROOT, folder)
    csv_path = os.path.join(folder_path, f"phase1_{folder}.csv")

    df = pd.read_csv(csv_path)
    df["dice"] = pd.to_numeric(df["dice"], errors="coerce")
    df = df[df["dice"].notnull()]  # keeps only 63 image rows

    # Accumulate pixel-level confusion across all 63 images
    TP = FP = FN = TN = 0

    rows = []
    for _, r in df.iterrows():
        gt_path = os.path.join(GT_DIR, r["gt_file"])
        pred_path = os.path.join(folder_path, r["pred_mask_file"])

        if (not os.path.exists(gt_path)) or (not os.path.exists(pred_path)):
            continue

        gt = load_bin_mask(gt_path)
        pred = load_bin_mask(pred_path)

        # resize GT to pred if needed
        if gt.shape != pred.shape:
            gt = cv2.resize(gt, (pred.shape[1], pred.shape[0]), interpolation=cv2.INTER_NEAREST)

        precision, recall, f1, tp, fp, fn = per_image_prf(pred, gt)

        # TN: total pixels - (tp+fp+fn)
        total = gt.size
        tn = total - (tp + fp + fn)

        TP += tp; FP += fp; FN += fn; TN += tn

        rows.append({
            "filename": r["filename"],
            "dice": float(r["dice"]),
            "precision": precision,
            "recall": recall,
            "f1": f1
        })

    mdf = pd.DataFrame(rows)
    per_image_metrics[model_name] = mdf

    # Aggregate metrics from summed confusion
    precision_all = TP / (TP + FP + 1e-8)
    recall_all    = TP / (TP + FN + 1e-8)
    f1_all        = (2 * precision_all * recall_all) / (precision_all + recall_all + 1e-8)

    cm = np.array([[TN, FP],
                   [FN, TP]], dtype=np.int64)

    phase2_results[model_name] = {
        "n_images_used": int(len(mdf)),
        "precision_pixel": float(precision_all),
        "recall_pixel": float(recall_all),
        "f1_pixel": float(f1_all),
        "confusion_matrix": cm
    }

