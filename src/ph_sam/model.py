def run_phase1_phsam1_single_run(
    centroids_dict: dict,
    model_type: str,
    checkpoint_path: str,
    mask_index: int,
    sample_size: int = 63,
    seed: int = 42,
    save_masks: bool = False
):
    """
    Runs ONE random batch of size=sample_size for one SAM backbone (PHSAM1).
    Computes:
      - avg dice
      - best dice
      - avg time/image
      - total batch time
    Writes per-image CSV and a summary dict.
    """
    random.seed(seed)
    np.random.seed(seed)

    out_dir = os.path.join(OUT_ROOT, f"{model_type}_mask{mask_index}_seed{seed}")
    os.makedirs(out_dir, exist_ok=True)

    predictor = load_sam_predictor(model_type, checkpoint_path)

    #
    eligible = []
    for filename, c_list in centroids_dict.items():
        if not isinstance(c_list, (list, tuple)) or len(c_list) < 2:
            continue
        img_path = os.path.join(ORIG_IMAGE_DIR, filename)
        if not os.path.exists(img_path):
            continue
        base_id = os.path.splitext(filename)[0]
        gt_path = find_gt_path(base_id, filename)
        if gt_path is None or not os.path.exists(gt_path):
            continue
        eligible.append(filename)

    if len(eligible) < sample_size:
        raise ValueError(f"Not enough eligible images with GT. Eligible={len(eligible)}, requested={sample_size}")

    sampled = random.sample(eligible, sample_size)

    rows = []
    dice_list = []
    per_img_times = []

    t_batch0 = time.time()

    for filename in sampled:
        img_path = os.path.join(ORIG_IMAGE_DIR, filename)
        base_id = os.path.splitext(filename)[0]
        gt_path = find_gt_path(base_id, filename)

        # read gray for mapping
        g = cv2.imread(img_path, cv2.IMREAD_GRAYSCALE)
        if g is None:
            continue

        c1 = tuple(map(int, centroids_dict[filename][0]))
        c2 = tuple(map(int, centroids_dict[filename][1]))
        c1o, c2o = map_centroids_to_original(g, c1, c2)

        t0 = time.time()
        pred_bin, pred_img = segment_with_sam_on_original(
            predictor, img_path, c1o, c2o, mask_index=mask_index
        )
        t1 = time.time()

        # load + binarize GT
        gt = cv2.imread(gt_path, cv2.IMREAD_UNCHANGED)
        if gt is None:
            continue
        if gt.ndim == 3:
            gt_bin = (np.sum(gt, axis=2) > 0).astype(np.uint8)
        else:
            gt_bin = (gt > 0).astype(np.uint8)

        # resize GT if needed
        if gt_bin.shape != pred_bin.shape:
            gt_bin = cv2.resize(gt_bin, (pred_bin.shape[1], pred_bin.shape[0]), interpolation=cv2.INTER_NEAREST)

        d = dice_score_bin(pred_bin, gt_bin)

        dt = t1 - t0
        dice_list.append(d)
        per_img_times.append(dt)

        pred_name = ""
        if save_masks:
            pred_name = f"{base_id}_pred_{model_type}_mask{mask_index}_dice{d:.4f}.png"
            cv2.imwrite(os.path.join(out_dir, pred_name), pred_img)

        rows.append({
            "filename": filename,
            "id": base_id,
            "gt_path": os.path.basename(gt_path),
            "dice": float(d),
            "time_sec": float(dt),
            "pred_mask_file": pred_name
        })

    t_batch1 = time.time()
    batch_time = t_batch1 - t_batch0

    avg_dice = float(np.mean(dice_list)) if dice_list else 0.0
    best_dice = float(np.max(dice_list)) if dice_list else 0.0
    avg_time_img = float(np.mean(per_img_times)) if per_img_times else 0.0

    # write per-image CSV
    csv_path = os.path.join(out_dir, f"phase1_{model_type}_mask{mask_index}_seed{seed}.csv")
    with open(csv_path, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["filename", "id", "gt_file", "dice", "time_sec", "pred_mask_file"])
        for r in rows:
            w.writerow([r["filename"], r["id"], r["gt_path"], f'{r["dice"]:.6f}', f'{r["time_sec"]:.6f}', r["pred_mask_file"]])

        w.writerow([])
        w.writerow(["SUMMARY", "", "", "", "", ""])
        w.writerow(["avg_dice", avg_dice])
        w.writerow(["best_dice", best_dice])
        w.writerow(["avg_time_per_image_sec", avg_time_img])
        w.writerow(["total_batch_time_sec", batch_time])
        w.writerow(["n_images_used", len(rows)])

    summary = {
        "model_type": model_type,
        "checkpoint": checkpoint_path,
        "mask_index": mask_index,
        "seed": seed,
        "sample_size_requested": sample_size,
        "n_images_used": len(rows),
        "avg_dice": avg_dice,
        "best_dice": best_dice,
        "avg_time_per_image_sec": avg_time_img,
        "total_batch_time_sec": float(batch_time),
        "csv_path": csv_path,
        "out_dir": out_dir
    }

    print("✅ PHASE 1 RUN DONE")
    print(summary)
    return summary

